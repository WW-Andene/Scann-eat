const normalize = (s) => String(s ?? '').trim().toLowerCase()
  .normalize('NFD').replace(/[̀-ͯ]/g, '');

// FR (and EN) → canonical English id. Includes SYNONYMS so that
// common user-typed variants still resolve.
const FR_TO_EN = (() => {
  const m = {};
  for (const [en, fr] of Object.entries(EN_TO_FR)) {
    m[normalize(fr)] = en;
    m[normalize(en)] = en;
  }
  for (const [user, target] of Object.entries(SYNONYMS)) {
    m[normalize(user)] = target;
  }
  return Object.freeze(m);
})();

/**
 * Resolve a FR or EN name (any case/accents) to the dataset's
 * canonical id. Tokenises the query and tries the largest n-gram
 * window first, so "saumon fumé atlantique" matches "saumon fumé"
 * (smoked_salmon) rather than collapsing to "saumon" (salmon).
 */
export function resolveIngredient(name) {
  const q = normalize(name);
  if (q.length < 2) return null;
  if (FR_TO_EN[q]) return FR_TO_EN[q];
  const tokens = q.split(/\s+/).filter((t) => t.length >= 2);
  for (let size = tokens.length; size >= 1; size -= 1) {
    for (let start = 0; start + size <= tokens.length; start += 1) {
      const candidate = tokens.slice(start, start + size).join(' ');
      if (FR_TO_EN[candidate]) return FR_TO_EN[candidate];
    }
  }
  return null;
}

/** Display-ready array of pairing names (FR). Empty on miss. */
export function findPairings(name, limit = 6) {
  const en = resolveIngredient(name);
  if (!en) return [];
  const entry = PAIRINGS[en];
  if (!entry) return [];
  return entry.pairs.slice(0, limit).map((p) => p.fr ?? p.b.replace(/_/g, ' '));
}

/** Full entry (UI wants the score + cooccur weights too). */
export function matchPairings(name) {
  const en = resolveIngredient(name);
  if (!en) return null;
  const entry = PAIRINGS[en];
  if (!entry) return null;
  return {
    en,
    name: entry.fr ?? en.replace(/_/g, ' '),
    recipe_count: entry.recipe_count,
    pairs: entry.pairs,
  };
}
